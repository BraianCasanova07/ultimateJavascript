let colorCelular = 'amarillo';
// switch es como un if/else, donde hay que poner el caso si cumple la condicion corre el codigo, si no busca otro caso, y si no con default pones otro caso en caso de no haber condiciones
switch (colorCelular) {
  case colorCelular = 'azul':
    console.log('Tu celular es colo azul');
    break;
  case colorCelular = 'rojo':
    console.log('Tu celular es color rojo');
    break;
  default:
    console.log('Tu celular es de otro color')
}