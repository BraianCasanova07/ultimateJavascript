let a = 10;
//relacionales
console.log(a > 5);
console.log(a >= 5);
console.log(a <= 5);
console.log(a < 5);
//igualdad
console.log(a == 10);
console.log(a != 10);
console.log(a === 10); // cuando es triple signo se evalua tipo de dato y valor
console.log(a !== 10); // cuando es triple signo se evalua tipo de dato y valor